# =================== Gerenciamento de Memória - Mapa de Bits ====================
#  [+] Uma memória possui tamanho total de 512 KB.
#      O sistema divide essa memória em 128 blocos.
#      Qual é o tamanho da unidade de alocação em bytes?
#  [!] Resposta: 4096

# =================== Cálculo ===================
#     Tamanho total da memória = 512 KB = 512 * 1024 bytes = 524288 bytes
#     Número de blocos = 128
#     Tamanho da unidade de alocação = Tamanho total da memória / Número de blocos
#     Tamanho da unidade de alocação = 524288 bytes / 128 blocos = 4096 bytes