#  Memória Principal e Blocos
#  [+] Uma memória principal possui tamanho máximo de 317K.
#      Cada bloco (ou célula) de memória armazena 32 bits (tamanho da palavra).
#      Qual é o número total de blocos?
#  [!] Resposta: 10144

# =================== Cálculo ===================
#     Tamanho total da memória = 317 KB = 317 * 1024 bytes = 324608 bytes
#     Tamanho da palavra = 32 bits
#     Número de blocos = Tamanho total da memória / Tamanho da palavra